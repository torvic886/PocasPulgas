export class Mascota 
{
    private nombre: string;
    private edad: string;
    
    
    constructor(nombre: string, edad: string) 
    {
        this.nombre = nombre;
        this.edad = edad;
    }

    getName(): string 
    {
      return `${this.nombre}`;
    }
  
    getAge(): string 
    {
      return `${this.edad}`
    }
    
}